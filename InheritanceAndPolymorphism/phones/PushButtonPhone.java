package phones;

/**
 * The method call should return 
 * the following string:
 * Pushing buttons to call {number}.
 * 
 * Where {number} is substituted 
 * by the corresponding parameter value.
 * @author Alex Murdock
 */
public class PushButtonPhone {
	

	public static String call(String number) {
		return "Pushing buttons to call " + number;
	}
}